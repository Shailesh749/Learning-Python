# finding student grade marks

marks = float(input("Enter your marks: "))
if marks >= 90:
   print("Grade is A")
elif marks >= 80 and marks < 90:
   print("Grade B")
elif marks >= 70 and marks < 80:
   print("Grade c")
elif marks >= 60 and marks < 70:
   print("Grade d")
else:
   print("Grade is F")   
